#Make a calculator with class

class calc: #no parameters
    def add(x,y):
        answer = x + y
        print(answer)
    def subtract(x,y):
        answer = x - y
        print(answer)
    def multiply(x,y):
        answer = x * y
        print(answer)
    def divide(x,y):
        answer = x / y
        print(answer)

